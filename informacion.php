<p>
Federico Martinez Galvis es un pintor y fotógrafo colombiano, nacido en Bogota en 1983, su trabajo se ha caracterizado por el uso recurrente de la figura humana como medio de expresion tanto en la pintura como en la fotografía; siendo esta el lenguage con el cual logra expresarse su propio mundo interno, y llegando a hacer visibles cosas desconocidas, ya que es el auto-conocimiento la mision de su trabajo artistico, tanto en su propia vida como en la de los demas. De esta manera llevando a sus espectadores a realizar el mismo proceso a traves del contacto con su obra.</p>
<p>
Por otra parte tambien trabaja como fotógrafo profesional en su propia compañia (Reflex), realizando trabajos para actores, modelos y musicos que quieran acceder a una imagen que los represente.</p>